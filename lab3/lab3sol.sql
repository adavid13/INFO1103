SELECT PRODUCTID, PRODUCTDESCRIPTION, PRODUCTSTANDARDPRICE

FROM PRODUCT_T

WHERE PRODUCTFINISH= 'Oak' and PRODUCTSTANDARDPRICE>500;



--b

SELECT CUSTOMERNAME, CUSTOMERADDRESS, CUSTOMERCITY, CUSTOMERSTATE

FROM CUSTOMER_T

WHERE CUSTOMERNAME like '%Furniture%'

ORDER BY CUSTOMERNAME;

--c

SELECT COUNT(*)

FROM EMPLOYEE_T

WHERE EMPLOYEECITY = 'Knoxville';



--d

SELECT CUSTOMERSTATE, COUNT(*)

FROM CUSTOMER_T

GROUP BY CUSTOMERSTATE

ORDER BY CUSTOMERSTATE;



--e

SELECT PRODUCTFINISH, COUNT(*)

FROM PRODUCT_T

GROUP BY PRODUCTFINISH

HAVING COUNT(*) > 2;



--f

SELECT o.ORDERDATE, c.CUSTOMERNAME, c.CUSTOMERADDRESS, c.CUSTOMERCITY

FROM ORDER_T o 
  INNER JOIN CUSTOMER_T c 
     ON o.CUSTOMERID = c.CUSTOMERID

WHERE CUSTOMERSTATE = 'NY';


